
public class Dowhileloop {

	public static void main(String[] args) {
		
		//do while loop//
		
		int i=0;
		do
		{
			System.out.println(i);
			i=i+2;
		}while(i<=10);
		
		//infinite do while
		
		do
		{
			System.out.println("Hello");
		}while(true);
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
